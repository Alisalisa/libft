#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int nr;
	int neg;

	nr = 0;
	neg = 0;
	i = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\r') || (str[i] == '\f'))
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		nr = 10 * nr;
		nr = (int)str[i] - '0' + nr;
		i++;
	}
	if (neg == 1)
		return (-nr);
	else
		return (nr);
}
