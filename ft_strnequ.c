#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 != NULL && s2 != NULL)
	{
		n--;
		while (s1[n] || s2[n])
		{
			if (s1[n] != s2[n])
				return (0);
			n--;
		}
	}
	return (1);
}
